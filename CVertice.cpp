#include "CVertice.h"

/************************************************
 * CVertice()
 * Constructor de la clase CVertice
 * @x   Posicion en eje X
 * @y   Posicion en eje Y
 * @z   Posicion en eje Z
 ************************************************/ 
CVertice::CVertice(float x, float y, float z)
{
    this->x = x;
    this->y = y;
    this->z = z;
}